Коммерческие предприятия могут участвовать в социалистическом производстве, при этом они должны получить квоту на продажу в территориальном органе госплана; выплата заработных плат участников производится согласно нормативам и предоставленных табелей рабочего времени из полученной выручки в с/р. То есть, фактически, любой гражданин может получить госзаказ по фиксированным ценам и получить за труд обоснованную плату. Деятельность за пределами квоты госплана является коммерческой и производится за р/р  и облагается налогами.


