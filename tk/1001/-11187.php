Предполагается, что реформа трудового законодательства уменьшит роль профсоюзов в переговорах об условиях труда, включая размер заработной платы. Сейчас зарплаты должны жёстко соответствовать соглашениям на уровне определённых секторов производства. Эти договорённости заключаются между отраслевыми профсоюзами и объединениями предпринимателей. После реформы предприниматели будут освобождены от необходимости следовать этим соглашениям. Кроме того, планируется уменьшить выходное пособие, выдаваемое в случае увольнения.

Хотя реформы и предполагают упрощение самой процедуры увольнения, правительство утверждает, что раз они способствуют экономическому росту, то впоследствии увеличат и занятость. Эммануэль Макрон обещал таким образом к 2022 году уменьшить безработицу с нынешних 9,4% до 7%.

«Сейчас Макрон почти насильственно пытается протащить реформу французского трудового законодательства в духе классического либерализма: упростить процедуру увольнения, лишать пособия по безработице за два отказа от предложения о трудоустройстве», — отмечает Платонова.

По словам эксперта, Макрон в такой ситуации выглядит не как «новый политик», а как новый представитель старой ультралиберальной политической системы.


##На минном поле


Противники реформы трудового законодательства делают упор на то, что в результате её реализации снизятся социальные гарантии для рядовых французов. Левые экономисты также утверждают, что неолиберальные реформы никак не связаны с экономическим ростом и, по сути, речь идёт о реализации классовых интересов капиталистов за счёт большинства населения страны.

Неоднократные попытки провести реформу трудового законодательства предпринимались и ранее и всегда сопровождались масштабными протестами.

«В своё время такие реформы пытался провести Жак Ширак, и тогда было ожесточённое сопротивление, потом пытался Саркози — и тоже французы вышли на улицу. И я не исключаю демонстраций и новых митингов, французы будут верны себе», — отметил в беседе с RT научный сотрудник ИМЭМО РАН Павел Тимофеев.

Последней попыткой было проведение «закона эль-Хомри» в 2016 году. Этот законодательный акт, снижавший доплаты за переработку и разрешавший вносить в трудовые договоры особые условия увольнения и увеличивать рабочую неделю, вызвал массовые протесты по всей стране. Одним из его разработчиков был тогдашний министр экономики и финансов Франции Эммануэль Макрон.
![](https://cdni.rt.com/russian/images/2017.08/original/598490861835615f2b8b4567.JPG)*                                 Reuters                             
Профсоюзы призывают к новым протестам. На 12 сентября «Всеобщая конфедерация труда» — второе по численности профсоюзное объединение во Франции — планирует массовые манифестации.

«Речь идёт о реформе законодательства, которая наверняка будет производиться в ущерб работникам, это должно вызвать ожесточённое сопротивление французов, — считает Тимофеев. — Французы привыкли защищать свои социальные права. Это для них такая священная корова».

##
##Путь к падению


Трудовая реформа — не единственная непопулярная мера нового французского президента. Страна всё туже затягивает пояса. Сокращено финансирование местных администраций. Резко урезаны траты на высшее образование и научно-исследовательские работы в университетах. Военные расходы также подлежат сокращению. Последнее обстоятельство вызвало конфликт Макрона с начальником Генерального штаба генералом Пьером де Вилье и отставку последнего.

При этом обещанная Макроном налоговая реформа и меры стимулирования бизнеса путём дотаций застопорились. Евросоюз требует сократить дефицит бюджета, и президент следует этой политике. Это заставляет его, с одной стороны, сокращать расходную часть бюджета — социальные траты, дотации предпринимателям, а с другой — не снижать налоги, чтобы бюджет мог наполняться.

«Макрон выбрал Меркель, а не быстрые реформы», — характеризует экономическую политику нового французского президента Politico.eu.
![](https://cdni.rt.com/russian/images/2017.08/original/598490fe1835615f2e8b4567.JPG)*                                 Reuters                             
Такая внутренняя политика не удовлетворяет ни работодателей, ни рабочих, в результате чего рейтинг одобрения нового президента стремительно падает.

Когда Макрон пришёл к власти, его поддерживали 64% французов. Теперь доля его сторонников сократилась до 36%.

«Макрон рекордными темпами теряет поддержку населения», — утверждает Платонова.

Согласно последним опросам YouGov, политический курс президента Франции поддерживают лишь 36% французов, в то время как 49% жителей Пятой республики негативно отозвались о политике Макрона. Главными проблемами страны французы, принявшие участие в опросе, назвали безработицу, мигрантов и вопросы социального обеспечения.
![](https://cdni.rt.com/russian/images/2017.08/original/59849483370f2cdf1e8b45b2.jpg)*                                 AFP                             
По мнению экспертов, противоречивая трудовая реформа ещё сильнее снизит рейтинг французского президента, на которого ранее возлагались большие надежды.

«Разочарование в Макроне неизбежно: французские избиратели купились на медиаобраз, и теперь пришло время столкновения с действительностью. А она такова, что новый президент проводит политику не в интересах большинства французов и даже в значительной степени не французских, а брюссельских элит», — полагает Платонова.

По мнению Тимофеева, французы в первую очередь будут оценивать работу Макрона по тому, как изменилась их повседневная жизнь.

«Не исключено, что если трудовая реформа будет окончательно утверждена в сентябре-октябре, когда закончится сезон отпусков, то там всё будет достаточно жарко», — считает эксперт.


