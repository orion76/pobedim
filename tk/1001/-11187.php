#Макроновские меры: зачем президент Франции проводит антисоциальную трудовую реформу 

6 августа 2017         Александр Бовдунов     

<youtube key="l1TYPyGYENI">

  Во Франции снова разгорается скандал вокруг трудовой реформы. Левая оппозиция обратилась в Конституционный совет с просьбой признать незаконным решение парламента разрешить правительству провести масштабную трудовую реформу, которая может ухудшить социальное положение миллионов французов. По мнению экспертов, изменение трудового законодательства, инициированное Эммануэлем Макроном, вызовет массовые протесты в стране и приведёт к дальнейшему падению рейтинга президента. RT выяснял, почему Макрон настаивает на проведении непопулярной у жителей Франции реформы и к каким последствиям это может привести.

![](https://cdni.rt.com/russian/images/2017.08/article/59848fe11835612a2b8b4567.JPG)*                                 Reuters          
                   
##Проверка на конституционность


Депутаты французского парламента от движения «Непокорённая Франция» Жан-Люка Меланшона вместе с представителями коммунистической партии и депутатской группой «Новые левые» объявили, что планируют обратиться в Конституционный совет страны, чтобы опротестовать законопроект, принятый сенатом Франции. Согласно новому закону, правительство получает право изменять трудовой кодекс: снижать социальную ответственность бизнеса, уменьшать гарантии наёмных работников и роль профессиональных союзов в регулировании трудовых отношений.

![](https://cdni.rt.com/russian/images/2017.08/original/59849016370f2c1b1f8b45a5.jpg)*                               Жан-Люк Меланшон * РИА Новости                             

Либерализация рынка труда была одной из первых реформ, о которой заявил Эммануэль Макрон сразу после того, как в мае 2017 года стал президентом страны. Изменения, которые Макрон хочет внести в трудовой кодекс, оппозиционные депутаты назвали легализацией «несправедливых увольнений».

Оппозиция считает, что полномочия, которые даются правительству благодаря новому законопроекту, будут означать де-факто директивное проведение трудовой реформы исполнительной властью без согласования с законодательной. 

Спорный законопроект был одобрен сенатом 3 августа. Ранее за него проголосовала нижняя палата французского парламента — Национальное собрание, где партия Эммануэля Макрона «Республика идёт вперёд» имеет абсолютное большинство голосов. Ожидается, что к сентябрю 2017 года правительство издаст указы, реформирующие трудовое законодательство.

«Смогут ли депутаты-оппозиционеры добиться блокировки этих поправок? Скорее всего, нет, — отметила в интервью RT эксперт Центра геополитических экспертиз Дарья Платонова. — Так же как протесты на улицах центральных городов Франции и забастовки в прошлом не смогли остановить продвижение других неолиберальных реформ».


##«Макрон почти насильственно протаскивает реформу»


Реформы в области трудового законодательства — одно из давних требований ЕС к Франции. Либерализовать рынок труда требует в первую очередь Германия — сосед и один из главных экономических партнёров Пятой республики. По мнению сторонников реформы, её смысл в том, чтобы уменьшить давление на бизнес и снизить издержки, связанные с его социальными обязательствами. Вкупе с обещанной Макроном налоговой реформой это должно подстегнуть экономический рост. Сейчас Франция находится в состоянии экономической стагнации (рост ВВП — около 1%), остаётся острой и проблема безработицы (9,4% — выше, чем в среднем в ЕС).
