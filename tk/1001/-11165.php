###Принципы социализма 
Такой политический строй базируется на четырех основных принципах, которые задают всю его идеологию. Их можно сформулировать следующим образом:

*Отсутствие частной собственности – этот принцип был сформулирован  [Марксом](https://uchim.guru/ekonomika-pravo/marksizma-eto-kratko-i-ponyatno.html) и Энгельсом в их «Коммунистическом манифесте». Любая социалистическая доктрина использует это положение, оно характеризует весь строй без уточнения иных деталей. 

*Отсутствие семьи как привычной единицы общества – данный пункт есть в большинстве учений, однако это положение не настолько радикальное, насколько оно звучит. Данный принцип ставит за цель уменьшение роли семьи и связей между ее членами, чтобы часть этих функций отдать иным общественным институтам. В качестве примера можно привести кружки жен или кружки по интересам, в которых родители и дети участвуют и укрепляют связи со всеми людьми, а не только с членами семьи. Таким образом, семья превращается в бюрократическую составляющую государства. 

*Уничтожение религиозных движений – сегодня это утверждение стало частью всех современных учений и доктрин во многих государствах. Данный принцип работает для постепенного изгнания религии из жизни общества, а не такое радикальное решение, которое применяли большевики во главе с Троцким. Хорошим примером работы данного принципа будут скандинавские страны, где высокий уровень жизни и низкая религиозность, причем скандинавы считают, что первое условие соблюдается только при соблюдении второго. 

*Равенство – данное требование по сути является основой всего социалистического строя и всех его производных и сопутствующих движений. Под этим под равенством подразумевают как стремление уничтожить уже установленную иерархию общества, так и предоставление всем членам общества равных прав и возможностей, независимо от их бывшего положения в обществе. Из-за неравенства в государстве часто можно наблюдать интеллектуальную и духовную пропасть между слоями населения. И именно ее следует уничтожить, чтобы достичь равенства. Сегодня этот принцип лег в основу многих левых течений, например, он лежит в основе модели шведской политики.  

