##Главные понятия 
Понятие "социализм" впервые было применено в начале 30-х годов 19 века. В то время в это понятие включались различные течения левого толка, целью которых было реформирование общества так, чтобы в нем были созданы лучшие условия для совершенствования самого общества и его участников. Начало свое данная идея берет еще в 16 веке, когда были написаны первые мировые утопии.

 ![](https://uchim.guru/wp-content/uploads/2018/01/Sotsializm-300x216.jpg)

###Социализм 
В реальном мире написанные теории стали воплощаться в 19 веке как реакция пролетариев на увеличение социальных противоречий в результате доминирования в обществе  [буржуазных](https://uchim.guru/ekonomika-pravo/burzhuaziya-eto-kratko-i-ponyatno.html) отношений.  [Социалистические](https://uchim.guru/ekonomika-pravo/sotsializm-eto-kratko-i-ponyatno.html) взгляды (власть – народу; от каждого по способности, каждому по труду) начали проявляться и в итоге выливаться в революции, что вполне понятно.
 
Это доктрина, которая ставит своей целью достижение **социальной справедливости и равенства**. В политике же это общественный курс, воплощающий доктринальные принципы, а его приверженцев называют социалистами.
 
Однако встречается такое понятие, как умеренные социалисты – так называют эсеров, влиятельную партию в России начала 20 века, которые придерживались социалистических взглядов и выступали за свержение самодержавия и создание демократической республики. Умеренные социалисты не смогли удержать власть и вскоре были подмяты большевиками.
 
Многие путают социализм с коммунизмом, но это не так. Социалисты — это последователи экономического и социально-политического положения в государстве, когда вопросы распределения производства и доходов решает все население. А коммунистические взгляды – это общественное устройство, при котором общественная собственность должна строиться за счет средств производства.

 Внимание! Маркс считал социалистическую теорию переходным этапом между коммунизмом и капитализмом.  


###Виды социализма 

Политико-общественное устройство такого типа имеет несколько разновидностей:

*Государственный – это течение, основанное на безусловном контроле экономики государством, включающее плановое хозяйство и командно-административную систему. Самое распространенное течение и форма строя, встречающаяся до сих пор. 

*Рыночный – неофициальный термин, которые обозначает наличие в государстве предприятий с коллективной формой собственности при царствующей в стране рыночной экономике. Последователи данного течения утверждают, что самоуправление на производстве принципиально отличается от обычного, характерного для рыночной экономики. 

*Самоуправленческий – это внутреннее течение, которое отрицает необходимость сильного государства, а также и монополию его собственности. Основными чертами такого движения является участие всех граждан в процессе принятия решений в условиях децентрализованной системы управления. При этом государство сохраняется для внешней политики, а его внутренние функции выполняют органы самоуправления. 

*Коммунизм – общественное устройство, которое основано на полном равенстве в обществе и общественной собственности, которая создается средствами производства. 

*Социал-демократия – это идейное и политическое движение, которое существует в рамках общего строя, но которое трансформировалось из социализма в позиции установления капитализма легально, но при этом сохранившее принципы социализма – устранение несправедливости в обществе, установление свободы и равенства. 

*Националистический – более известный как нацизм, который перенял все черты социализма, кроме общественной собственности. У нацистов собственность – это не государственное имущество, но производство, работающее на достижение общих целей. Нацисты считали коммунистов своими главными политическими конкурентами и физически истребляли в 20–40 годы XX века в Германии. 

*Общинный социализм – это движение, которое возникло в России в XIX в. с подачи А. Герцена, который призывал обратить внимание на порядки общества крестьян. Герцен утверждал, что именно крестьяне станут началом подобного строя в империи, поскольку в то время крестьянство на самом деле имело некие зачатки такой политики.  


Таким образом, виды социализма разнообразны и иногда серьезно отличаются друг от друга. 

