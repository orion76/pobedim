**СТАТЬЯ 5.**  Социалистическая собственность в ССР имеет форму государственной собственности (всенародное достояние). Частная собственность допускается, если создана собственным трудом.

Никто не вправе использовать социалистическую собственность в целях личной наживы и в других корыстных целях.

Частная собственность с одной стороны - это возможность трудовой инициативы, а с другой - инструмент паразитизма. Паразитизм считается неприемлемым, поэтому дивиденды и арендная плата запрещены. Прибыль частного предприятия может быть направлена исключительно на развитие всего предприятия, а не на потребление (использование отдельными сотрудниками).


