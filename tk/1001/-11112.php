   Вопрос краеугольный. Допуская частную собственность как явление, многие истинные коммунисты упрекнут в "мелкобуржуазности" мышления. Однако есть два НО: 1) уже существуют мелкие фирмы, созданные трудом 2) государство не ставит задачу производить весь ассортимент продукции и услуг, а ограничивается только необходимым для жизни, оставляя прочее для предпринимателей.

Государство не имеет задачу поддерживать частное предпринимательство, то есть, каждому частнику потребуется самостоятельно доказывать свою эффективность, уплачивая все налоги, которые на принципах сегодняшних капиталистов.


