##I. ОСНОВНЫЕ ЦЕЛИ, ЗАДАЧИ И ПРИНЦИПЫ ДЕЯТЕЛЬНОСТИ, ПРАВА И ОБЯЗАННОСТИ ПАРТИИ

1.1. Основными целями и задачами НП являются:

- реализация положений Программы НП;
- участие в политической и государственной жизни посредством влияния на формирование политической воли граждан в целях завоевания власти и 
 передачи власти народу, обеспечения подлинного народовластия;
- формирование общественного мнения, разъяснение населению положений Программы и других документов НП, а также позиции партии по различным вопросам;
- политическое образование и воспитание граждан;
- выражение мнений граждан по любым вопросам общественной жизни, доведение этих мнений до сведения широкой общественности, органов государственной власти и органов местного самоуправления;
- укрепление своего влияния в обществе за счёт действий каждого члена НП, каждого первичного, местного и регионального отделения партии;
- выдвижение кандидатов на выборах всех уровней,  участие в выборах и в работе избранных органов;
- координация действий своих фракций, депутатских групп и депутатов в законодательных (представительных) органах государственной власти и представительных органах муниципальных образований;
- развитие собственной материально-технической и финансовой базы.

1.2. Деятельность НП основывается на принципах добровольности, равноправия, самоуправления и открытости. НП свободна в определении своей внутренней структуры, целей, форм и методов деятельности.
НП действует на основе  идейной общности и партийного товарищества, что означает:

- назначаемость всех органов партии снизу доверху;
- периодическую отчётность органов и работающих в них членов НП;
- коллегиальность и открытость в работе всех структурных подразделений и органов партии;
- личную ответственность каждого члена партии за выполнение своих обязанностей и партийных поручений;
- свободу критики;
- самостоятельность структурных подразделений в решении вопросов своей внутренней жизни и деятельности, за исключением случаев, предусмотренных Уставом партии;
- партийную дисциплину, предусматривающую строгое соблюдение всеми структурными подразделениями, их органами и каждым членом НП принятых решений;
- гарантированное право меньшинства на изложение своих взглядов, учёт этих взглядов партийными органами при выработке решений и безусловное подчинение меньшинства большинству после принятия решений;
- обязательность выполнения решений вышестоящих органов для нижестоящих.

1.3.  НП  имеет право:

- свободно распространять информацию о своей деятельности, пропагандировать свои взгляды, цели и задачи;
- участвовать в выработке решений органов государственной власти и органов местного самоуправления;
- участвовать в выборах и референдумах;
- создавать региональные, местные отделения, в том числе с правами юридического лица и закреплять за указанными отделениями имущество для реализации программных и уставных целей и задач партии, а также принимать решения об их реорганизации и ликвидации;
- создавать первичные отделения, а также принимать решения об их ликвидации;
- организовывать и проводить собрания, митинги, демонстрации, шествия, пикетирования и иные публичные мероприятия;
- учреждать издательства, информационные агентства, полиграфические предприятия, средства массовой информации и образовательные учреждения дополнительного образования;
- пользоваться на равных с другими политическими партиями условиях государственными и муниципальными средствами массовой информации;
