##7. ДЕНЕЖНЫЕ СРЕДСТВА И ИМУЩЕСТВО ПАРТИИ

7.1. Имущество и денежные средства партии распределяются между членами партии и юридическими лицами на основе договоров. То есть, не существует единого фонда с формальным правом собственности одного лица.

