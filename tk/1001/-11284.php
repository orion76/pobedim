7.3. В собственности партии могут находиться юридические лица, которые ведут деятельность в соответствии законодательства и учитывают имущество на обособленных балансах, но под контролем НП. 


7.4. Имущество партии используется только для реализации целей и решения задач, предусмотренных Программой и Уставом НП.

7.5. Отделения НП отвечают по своим обязательствам, находящимся в их распоряжении имуществом. 

7.6. Ответственность за осуществление финансовой деятельности НП, её отделений несут руководители, назначаемые голосованием отделения.  

7.7. Учёт имущества партии ведётся на основании договоров с формальными владельцами имущества, готовых использовать имущество при выполнении общих задач.

7.8. Отделения НП обладают правом оперативного управления имуществом, закреплённым за ними собственниками, имеют балансы и сметы.

7.9.  Каждый член партии указывает максимально возможную сумму средств, которую он готов потратить при осуществлении проектов отделения, а также перечень навыков, которые он готов применить в деле с указанием расходов, которые потребуется понести отделению партии в пользу третьих лиц.  Перечисление средств на счета партии не производится, члены партии оплачивают расходы непосредственно поставщикам, самостоятельно корректируя остаток средств для будущих расходов.




