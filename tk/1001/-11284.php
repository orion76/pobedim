Действия отдельных членов НП, равно как и действия иных лиц, принимающих участие в мероприятиях, организованных НП или её структурными подразделениями, противоречащие Программе НП, настоящему Уставу или действующему законодательству, не могут расцениваться как действия НП или её структурных подразделений




##2. ОРГАНИЗАЦИОННАЯ СТРУКТУРА  НП


2.1. Организационная структура НП  состоит из Центрального комитета (ЦК) и подчинённых отделений.  


2.2. Количество отделений НП определяется объективной необходимостью объединения граждан для решения конкретных задач каждой конкретной территории. Для решения вопросов нескольких смежных территорий создаётся местное или региональное отделение.


2.6. Юридические лица НП создаются и/или используются с целью выполнения норм действующего законодательства и являются инструментом для выполнения задач НП.


2.8. Спорные вопросы, возникающие между отделениями решаются голосованием на более высоком уровне.

2.9. Для реализации проектов, специальных функций управления и контроля  НП формирует специализированные комитеты.



## 3. ЦЕНТРАЛЬНЫЙ КОМИТЕТ

3.1. Постоянно действующим руководящим органом партии является Центральный Комитет НП, членами которого являются все члены партии.

3.2. Центральный Комитет решает все вопросы деятельности партии, в том числе:

- разрабатывает программу партии и документы по важнейшим вопросам социально-экономической и политической жизни на основе решений голосований;
- даёт официальное толкование Устава НП;
- разрабатывает предложения по вопросам внутренней и внешней политики партии;
- определяет тактику партии на текущий период;
- разрабатывает проекты предвыборной программы (платформы) партии;
- координирует деятельность партии;
- взаимодействует с общественными объединениями в стране;
- представляет НП в отношениях с органами государственной власти и органами местного самоуправления;
- представляет НП в отношениях с зарубежными партиями и организациями;
- учреждает центральные партийные средства массовой информации и утверждает их главных редакторов;
- принимает решение о создании, реорганизации или ликвидации первичных, местных и региональных отделений ;
- утверждает Положение о партийных поощрениях и взысканиях;
- подтверждает членство в НП;
- формирует комиссии, включая контрольно-ревизионные.


3.3. Координация работы отделений осуществляется Центральным Комитетом партии, в состав которого могут входить все члены партии.
Решения ЦК партии принимается на общем голосовании и являются обязательными к исполнению всеми членами партии.

3.4. Все вопросы отделений решаются членами соответствующих отделений путём открытого голосования на сайте партии. Решением по вопросу является то решение, за которым сохраняется большинство голосов. 

3.5. После принятия решения производится назначение ответственного лица за исполнение решения из  числа проголосовавших за данное решение.
Назначение производится также путём открытого голосования.



##4. ОТДЕЛЕНИЯ НП

4.1. Основу партии составляют отделения НП.
 Отделение может быть создано любым гражданином путём регистрации его на сайте НП.  Лицо, зарегистрировавшее отделение, признаётся администратором отделения, отвечающим за идентификацию присоединяющихся граждан к отделению. Администратор должен быть действительным членом НП.

4.2 Администратор отделения может быть переизбран участниками собрания в результате голосования в соответствии регламентом работы отделения. Администратор не имеет привилегий.  Администратор вправе привлекать помощников из числа активистов, однако ответственность за правильность состава участников собраний лежит на администраторе.

4.3.  В случае выявления нарушений в идентификации   членов   отделения, такое отделение упраздняется или реорганизуется с корректировкой итогов голосований с учётом выявленных нарушений.

4.4. Высшим руководящим органом отделения НП является общее собрание всех членов отделения, состоящих на учёте в данном партийном отделении.  Собрание производится путём голосования на сайте НП  с обсуждением вопросов в социальных сетях отделения и встречах.

