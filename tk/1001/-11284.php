5.2. Членом НП может быть дееспособный гражданин, признающий и выполняющий Программу и Устав НП, состоящий на учёте и участвующий в работе одного из первичных партийных отделений.

5.3. Все члены НП обладают равными правами:

- свободно выражать своё мнение, обсуждать любые вопросы деятельности партии;
- участвовать в выработке решений партии;
- избирать и быть избранным в руководящие и контрольно-ревизионные органы партии, а также в органы её структурных подразделений;
- пользоваться защитой и поддержкой партии;
- получать поощрения за свою работу в партии;
- участвовать в работе партийного органа при обсуждении вопросов, затрагивающих интересы соответствующего члена НП;
- критиковать любой партийный орган и любого члена НП;
- регулярно получать информацию о деятельности НП, её структурных подразделений и выборных органов и давать оценку их работе;
- обращаться с заявлениями и предложениями в любой партийный орган;
- обжаловать решения и действия выборных партийных органов в порядке, предусмотренном настоящим Уставом;
- участвовать в работе общественных объединений, деятельность которых не входит в противоречие с программными и уставными целями НП;
