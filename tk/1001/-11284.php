Политическая партия «НАРОДНАЯ СОВЕТСКАЯ СОЦИАЛИСТИЧЕСКАЯ ПАРТИЯ» (далее – НП, Народная партия) создана на добровольных началах гражданами, объединившимися на основе общности интересов для реализации её программных и уставных целей.


Народная - партия создана для объединения общества в интересах народа, сбережения народа и процветания народа; 
Советская - принцип принятия решений через голосование в советах (собраниях) всех уровней;
Социалистическая - приоритет общественного интереса перед частным при принятии решений;
Партия - активисты сплотившиеся для борьбы.


Народная партия имеет своей главной целью  объединить население в  народ для установления народовластия через доступ через интернет к голосованию каждым гражданином по всем вопросам государственного и местного управления, а также создание этой системы голосования.

Народная партия руководствуется социалистическими принципами равенства, братства и справедливости; отстаивает интересы трудящихся. Главная идеологическая цель Народной партии — справедливое общество как основа счастья семей.


Народная партия осуществляет свою деятельность в соответствии с Программой партии и Уставом.

Полное наименование партии: Политическая партия «НАРОДНАЯ СОВЕТСКАЯ СОЦИАЛИСТИЧЕСКАЯ ПАРТИЯ» . 
Сокращённое наименование партии: НАРОДНАЯ ПАРТИЯ,  НП  или НССП.

Местом нахождения постоянно действующего руководящего органа партии - Центрального Комитета Народной партии  (далее - ЦК НП)  определяется голосованием на сайте партии

