**СТАТЬЯ 75.**  Народные Комиссариаты руководят порученной им отраслью государственного управления на всей территории ССР или непосредственно или через назначаемые ими органы.

**СТАТЬЯ 76.**  Народные Комиссариаты руководят порученной им отраслью государственного управления, как правило, через одноименные Территориальные Народные Комиссариаты и управляют непосредственно лишь определенным ограниченным числом предприятий по списку, утверждаемому Президиумом Верховного Совета ССР.

