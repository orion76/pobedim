**СТАТЬЯ 72.**  Народные Комиссары ССР руководят отраслями государственного управления, входящими в компетенцию ССР.

**СТАТЬЯ 73.**  Народные Комиссары ССР издают в пределах компетенции соответствующих Народных Комиссариатов приказы и инструкции на основании и во исполнение действующих законов, а также постановлений и распоряжений Совета Народных Комиссаров ССР и проверяют их исполнение.

