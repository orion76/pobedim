**СТАТЬЯ 70.**  Совет Народных Комиссаров ССР образуется Верховным Советом ССР в составе: Председателя Совета Народных Комиссаров ССР; Заместителей председателя Совета Народных Комиссаров ССР; Председателя Государственной плановой комиссии ССР; Председателя Комиссии советского контроля; Народных Комиссаров ССР; Председателя Комитета заготовок; Председателя Комитета по делам искусств; Председателя Комитета по делам высшей школы.

