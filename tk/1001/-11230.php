 4. Вопросы войны и мира.
 5. Принятие в состав РФРСН новых республик.
 6. Организация обороны РФРСН, руководство всеми вооружёнными силами РФРСН.
 7. Координация внешней торговли.
 8. Организация внутренней безопасности граждан РФРСН.
