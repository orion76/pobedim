## ОСНОВЫ РОССИЙСКОЙ ФЕДЕРАТИВНОЙ  РЕСПУБЛИКИ САМОУПРАВЛЯЕМОГО НАРОДА

26.09.2018 года					

Статья 1. Россия объявляется Федеративной Республикой Самоуправляемого Народа (РФРСН), или Российской Народной Республикой (РНР), или просто Россией. В центре и на местах осуществляется самоуправление народа в соответствии с общественно персонализированными отношениями собственности: от управления собой (каждый дееспособный гражданин) до управления Российской Народной Республикой (Всероссийский Съезд Народных Депутатов).

Статья 2. Российская Федеративная Республика  Самоуправляемого Народа учреждается  на  основе союза   наций  как  федерация  субъектов РФРСН.

Статья 3. Верховная    власть    в   Российской   Федеративной  Республике Самоуправляемого Народа принадлежит референдуму, свободно выбираемому   Всероссийскому Съезду  Народных Депутатов,  Всероссийскому Центральному  Комитету Координаторов (в период между съездами) и .Конституционному Суду Российской Народной Республики.

Статья 4. Первый  Всероссийский  Съезд Народных  Депутатов постановляет:
Земля России, её недра, воды, воздушное пространство над ней являются общенародным достоянием.
Всё, что создано на поверхности земли России, открыто и добыто в её недрах и водах предками ныне живущих в России людей, а также всё, что открыто и произведено ныне живущими в России гражданами, является собственностью народа России в каждой его индивидуальности.
Народ России, каждой своей индивидуальностью, является создателем материальных и духовных благ страны.
Основываясь на праве общественно персонализированной собственности и на основании пунктов 1, 2, 3, Статьи 4 Конституции, каждый гражданин России является равным с другими, собственником-совладельцем всего национального богатства России и собственником результатов своего труда (дохода) — в соответствии с величиной общественно необходимого труда и созданной личным трудом избыточной стоимостью.

Статья 5. Первый Всероссийский Съезд Народных Депутатов принимает закон о рабочем контроле при переходе фабрик,  заводов, рудников,  железных  дорог  и  прочих  средств   производства   и транспорта    в   собственность граждан Российской Народной Республики, а также за деятельностью выборных высших координирующих органов..

Статья 6. Первый Всероссийский Съезд Народных Депутатов:
а) рассматривает закон   об   аннулировании   (уничтожении)   займов,  заключенных Центробанком России за весь период его деятельности;
б) подтверждает  переход  всех  банков   в   собственность Российской Социалистической Федеративной Республики Самоуправляемого Трудового Народа;
в) вводит всеобщую трудовую повинность;
г) декретирует восстановление Социалистической Красной Армии трудового народа.

Статья 7. Первый Всероссийский Съезд  Народных Депутатов постановляет: власть принадлежит целиком  и  исключительно народу в каждой его индивидуальности в виде самоуправлений в центре и на местах.

Статья 8. Первый  Всероссийский  Съезд Народных Депутатов,  стремясь создать полный и прочный союз трудящихся всех наций России, признаёт за нациями участие в федеральном правительстве и в остальных федеральных учреждениях на общих основаниях.

Статья 9. Основная  задача,  рассчитанной  на  настоящий  переходный момент  Конституции  Российской   Федеративной  Республики Самоуправляемого  Народа,  заключается   в  установлении повсеместно экономического персонализма  как социально-экономической основы социализма – гражданского общества  без наёмного труда и эксплуатации.

Статья 10. Российская Народная Республика  есть  свободное  гражданское общество. Вся власть в пределах РФРСР  принадлежит всему населению  страны,  объединенному в самоуправления на федеральном и местном (республики, края, области, города, районы, сёла) уровнях.

Статья 11. Всероссийский Съезд  Народных Депутатов и референдум являются высшей координирующей властью РФРСН, наблюдающей  и утверждающей общегражданские интересы. В  период  между  съездами  верховной  властью   Республики является   Всероссийский   Центральный      Комитет координаторов, реализующий и координирующий общегражданские интересы. Конституционный Суд Российской Народной Республики является верховной судебной властью, соблюдающей конституционность общегражданских интересов.

Статья 12. Верховная законодательная, исполнительная и судебная власти действуют  на основе принципа разделения властей и исходят в своей деятельности из приоритета прав гражданина Российской Народной Республики.

Статья 13. Конституция Российской Федеративной Республики Самоуправляемого Народа имеет прямое действие.

Статья 14. Право общественно персонализированной собственности осуществляется каждым гражданином персонально на основании Конституции и законов Российской Народной Республики.

Статья 15. Право общественно персонализированной собственности реализуется каждым гражданином следующим образом:
Каждому оформляется счёт в народном банке с зачисленной суммой неизымаемого на личные нужды первоначального (стартового) капитала.
Первоначальный (стартовый) капитал используется исключительно как вложение в своё персональное производство (индивидуальное или совместное, новое или уже действующее) с целью производства и прямого извлечения результата своего личного труда в денежной форме.

Статья 16. Каждый гражданин РНР беспрепятственно владеет, пользуется и распоряжается,  на праве общественно персонализированной собственности, землёй и другим имуществом, если это не влечёт сразу или впоследствии неестественного негативного физического разрушения или изменения этой собственности, среды обитания отдельного человека, людей или экосистемы в целом.

Статья 17. Весь доход, произведенный и извлечённый гражданином на законном основании, является его собственностью.

Статья 18. Собственность (в любом виде) гражданина Российской Народной Республики неприкосновенна и охраняется законом.

Статья 19. Часть (в виде процента) произведенной гражданами прибыли, которую они уступают (отчисляют) на общегражданские нужды (экологию, здравоохранение, воспитание, обучение, защиту, социальное обеспечение, культуру, фундаментальные научные исследования и содержание координаторов /Всероссийского   съезда   Народных Депутатов, Всероссийского Центрального Комитета Координаторов, Совета Народных Координаторов/) составляют средства гражданского бюджета.

Статья 20. Высшие органы Российской Народной Республики координируют политическую и хозяйственную деятельность страны через законотворчество, нормотворчество, фиск и, совместно с заинтересованными собственниками-совладельцами, контролируют исполнение.

Статья  21. Гражданский бюджет находится в ответственном ведении Всероссийского Центрального Комитета Координаторов.

Статья 22. Земля, воздушное пространство над ней, её недра, леса, воды, другие средства производства и их продукция, в отношении которых не имели места или прекращены права персональной или совместной собственности, являются свободной (невостребованной) собственностью  народа и находятся  в ответственном ведении Совета Народных Координаторов.


###ПРАВА И ОБЯЗАННОСТИ ГРАЖДАН РОССИЙСКОЙ ФЕДЕРАТИВНОЙ  РЕСПУБЛИКИ САМОУПРАВЛЯЕМОГО НАРОДА (РФРСН)

Статья 23. Все граждане Российской Народной Республики (РНР) рождены свободными и равными в своих правах в отношении друг друга, а также в отношении земли и другого имущества РНР.

Статья 24. Каждый гражданин РНР, без какого бы то ни было различия в отношении расы, цвета кожи, пола, языка, религии, убеждений, национального или социального происхождения, имущественного положения, обладает всеми правами и свободами на основе Конституции и законов РНР.

Статья 25. Каждый гражданин России имеет право на жизнь и личную неприкосновенность.

Статья 26. Каждый гражданин России беспрепятственно реализует своё право  общественно персонализированного собственника-совладельца на основе Конституции и законов РНР.

Статья 27. Гражданин России не может подвергаться пыткам или жестокому , в том числе унижающему его достоинство, обращению и наказанию.

Статья 28. Каждый гражданин России в любых обстоятельствах правосубъектен.

Статья 29. Все граждане России равны перед Конституцией и Законами РНР и имеют право, без всякого различия, на равную защиту закона.

Статья 30. Каждому гражданину гарантируется полное и возмездное восстановление в правах компетентным судом в случае нарушения его основных прав, представленных ему Конституцией и законами РНР.

Статья 31. Гражданин России не может быть подвергнут произвольному аресту, задержанию или изгнанию.

Статья 32. Каждый гражданин России, для определения его прав и обязанностей и для установления обоснованности предъявленного ему уголовного обвинения, имеет право, на основе полного равенства, на то, чтобы его дело было рассмотрено гласно и с соблюдением всех требований на основе Конституции и законов РНР.

Статья 33. Гражданин России, обвиняемый в совершении преступления, считается невиновным до тех пор, пока его виновность не будет установлена законным порядком, путём гласного судебного разбирательства, при котором обеспечивается всевозможная защита.

Статья 34. Гражданин России не может быть подвергнут произвольному вмешательству в его личную и семейную жизнь, произвольным посягательствам на неприкосновенность его жилища и личного имущества, тайну его корреспонденции или на его честь и репутацию. Каждый гражданин имеет право на защиту закона от такого вмешательства или таких посягательств.

Статья 35. Каждый гражданин России имеет право свободно передвигаться на территории РНР и выбирать себе место для жительства. Каждый гражданин России имеет право, не изменяя гражданства, покидать РНР и возвращаться в неё снова.

Статья 36. Гражданин России не может быть произвольно лишён своего гражданства или права изменить своё гражданство.

Статья 37. Гражданам России гарантируется свобода  совести:  церковь  отделяется  от  государства и школа от церкви,  а  свобода  религиозной  и  антирелигиозной   пропаганды признается за всеми гражданами.

Статья 38. Гражданам России гарантируется свобода  выражения  своих  мнений.   Российская   Федеративная Республика Самоуправляемого  Народа  предоставляет в руки самоуправляемого народа все технические и материальные средства к изданию газет, брошюр,  книг и всяких других произведений печати и  обеспечивает их свободное распространение по всей стране.

Статья 39. Гражданам России гарантируется свобода собраний, митингов, шествий и тому подобного по их требованию как на улицах, так и в помещениях.

Статья 40. Гражданам России гарантируется свобода организации в общественные объединения и их союзы.

Статья 41. Гражданам России гарантируется  полное,   всестороннее   и   бесплатное образование.

Статья 42. Российская   Федеративная Республика Самоуправляемого Народа  признает  труд обязанностью всех дееспособных граждан. Каждый гражданин России имеет право на свободный выбор работы, на справедливые и благоприятные условия труда и на защиту при форс-мажорных обстоятельствах.

Статья 43. Российская   Федеративная Республика Самоуправляемого Народа признает  обязанностью  всех граждан   Республики   защиту   социалистического   Отечества   и устанавливает  всеобщую  воинскую  повинность. Защищать  социалистическое Отечество  с  оружием  в  руках – почётное право граждан Республики.

Статья 44. Российская   Федеративная Республика Самоуправляемого Народа предоставляет все  политические   права   российских   граждан   иностранцам, пожелавшим жить и работать  на  территории  Российской  Республики и внёсшим сумму, равную первоначальному капиталу каждого гражданина России, которая автоматически ложится на счёт такого иностранца. Местные самоуправления  предоставляют   таким  иностранцам,  без  всяких  затруднительных формальностей, жильё и работу на общих основаниях.

Статья 45. Российская   Федеративная Республика Самоуправляемого Народа предоставляет   право   убежища   всем  иностранцам, подвергающимся  преследованию  за  политические   и   религиозные убеждения.

Статья 46. Российская   Федеративная Республика Самоуправляемого Народа, признавая равные права за гражданами независимо от их расовой  и национальной принадлежности,  объявляет противоречащим основным законам Республики установление или допущение каких-либо привилегий или преимуществ на этом основании, а равно какое бы то ни было угнетение  национальных  меньшинств  или  ограничение  их равноправия.

Статья 47. Российская   Федеративная Республика Самоуправляемого Народа лишает   отдельные   лица   и   отдельные   группы лиц гражданских  прав, поскольку они используются ими в ущерб интересам собственников-совладельцев России. Такие лица вправе свободно выехать из страны без первоначального капитала.

Статья 48. Каждый гражданин России имеет право на первоначальный (стартовый) капитал в соответствии с наличным национальным богатством на основе Конституции и законов РНР.

Статья 49. Каждый трудящийся гражданин России имеет право на гарантию  за свой общественно необходимый (средний) труд и прибыль, соответствующую лично произведенному избыточному продукту.

Статья 50. Каждый гражданин России имеет право на прожиточный минимум (процент на первоначальный капитал) при форс-мажорных обстоятельствах и по другим основаниям в соответствии с Конституцией и законами РНР.

Статья 51. Каждый гражданин России имеет право на защиту его моральных и материальных интересов, являющихся результатом научных, литературных или художественных трудов, автором которых он является.

Статья 52. Каждый гражданин России при осуществлении своих прав и свобод обязан соблюдать Конституцию и исполнять законы РНР.

Статья 53. Никто (отдельное лицо, группа лиц, субъект РНР) не в праве заниматься деятельностью или совершать действия, направленные на уничтожение народоправных отношений собственности.




###ОБЩЕГРАЖДАНСКОЕ УСТРОЙСТВО

Статья 54. Российская Федеративная Республика Самоуправляемого Народа – федеративное гражданское общество, образованное на основе добровольного, закреплённого временем объединения равных субъектов Федерации: республик, краёв, областей.      

Статья 55. Ведению РФРСН в лице её высших органов самоуправления подлежат:
 1. Принятие Конституции и законов РФРСН, внесение в них изменений и дополнений, контроль за их соблюдением. Обеспечение соответствия конституций субъектов РФРСН с Конституцией РФРСН.
 2. Установление общего порядка взаимодействия РФРСН и субъектов РФРСН с иностранными государствами.
