 16. Разработка основных правил внутренней и внешней торговли, установление порядка поддержания равновесия между предложением и спросом, ежегодное утверждение единых цен и всего механизма регулирования рынков по основным видам товаров.
 17. Установление основ законодательства о судоустройстве и судопроизводстве, основ гражданского уголовного законодательства.
 18. Законодательство о всероссийском гражданстве, законодательство о правах иностранцев в России.
 19. Установление общего порядка ответственного распоряжения невостребованной общественно персонализированной собственностью РФРСН.
 20. Координация размещения и развития производительных сил РФРСН.
 21. Организация общегражданского страхования РФРСН.
 22. Предоставление займов.
 23. Издание общегражданских актов об амнистии.
 24. Утверждение изменений границ между субъектами РФРСН.
 25. Утверждение образования новых субъектов в существующих пределах РФРСН.
 26. Принятие решений о проведении общегражданского референдума.

