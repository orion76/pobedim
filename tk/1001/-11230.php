а) объявляет состояние войны в случае военного нападения на РФРСН или в случае необходимости выполнения международных договорных обязательств о взаимной обороне от агрессии;
б) общую и частичную мобилизацию;
в) объявляет в отдельных местностях или по всей территории РФРСН военное положение в интересах обороны РФРСН.

Статья 81. По истечении  полномочий или в случае досрочного роспуска Всероссийского Съезда Народных Депутатов Всероссийский Центральный Комитет Координаторов сохраняет свои полномочия вплоть до образования вновь избранным ВСНД нового ВЦКК.

Статья 82. Члены    Всероссийского   Центрального   Комитета Координаторов (ВЦКК)  работают в отделах народных самоуправляемых отраслей   или выполняют    особые    поручения    Всероссийского   Центрального Комитета Координаторов.



