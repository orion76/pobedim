Статья 57. Каждый субъект РФРСН имеет свою Конституцию или Устав, учитывающие свои особенности и построенные в соответствии с Конституцией РФРСН.

Статья 58. Территория субъекта РФРСН не может быть изменена без его согласия.

Статья 59. Законы РФРСН имеют одинаковую силу на территории всех субъектов РФРСН.

Статья 60. В случае расхождения закона субъекта РФРСН с общегражданским законом РФРСН, действует общегражданский закон РФРСН.

Статья 61. Для граждан РФРСН устанавливается единое гражданство. Каждый гражданин субъекта РФРСН является гражданином Российской Федеративной Республики Самоуправляемого Народа.

Статья 62. Вопросы административно-территориального устройства республик РФРСН относятся к ведению самих республик.


###РЕФЕРЕНДУМ (ВСЕНАРОДНОЕ ВЕЧЕ)

Статья 63. Референдум наряду с свободно выбираемым Всероссийским Съездом Народных Депутатов, Всероссийским Центральным Комитетом Координаторов и Конституционным Судом РФРСН является высшей властью Российской Федеративной Республики Самоуправляемого Народа.

Статья 64. Всенародный референдум и референдумы субъектов РФРСН  объявляется в обязательном порядке ежегодно после опубликования суммы первоначального капитала на счету каждого гражданина России с единственным вопросом о доверии избранной координирующей власти РФРСН и территориальных самоуправлений.

Статья 65. Референдум объявляется Всероссийским Съездом Народных Депутатов и соответствующими высшими органами самоуправлений в периоды их работы или по требованию большинства граждан  (50% + 1 голос) России и территориальных самоуправлений по любому вопросу.
 


###ВСЕРОССИЙСКИЙ СЪЕЗД НАРОДНЫХ ДЕПУТАТОВ

Статья 63. Всероссийский  Съезд Народных Депутатов наряду с референдумом является  высшей  властью Российской Федеративной Республики Самоуправляемого Народа.

