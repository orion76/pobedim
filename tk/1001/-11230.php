Статья 65. Всероссийский  Съезд  Народных Депутатов избирается сроком на четыре (?) года и созывается  Всероссийским Центральным  Комитетом Координаторов  не реже двух раз в год.

Статья 66. Чрезвычайный  Всероссийский  Съезд  Народных Депутатов   созывается Всероссийским  Центральным  Комитетом Координаторов  по  собственному  почину  или  по  требованию  местных самоуправлений, насчитывающих не менее 1/3 (?) всего населения РНР.

Статья 67. Всероссийский Съезд Народных Депутатов осуществляет все права, присвоенные РФРСН согласно статье 55 Конституции в пунктах 1-5, 9, 14-19, 23-26, поскольку они не входят, в силу Конституции, в компетенцию Всероссийского Центрального Комитета Координаторов.

Статья 68. Депутат Всероссийского Съезда Народных Депутатов не может быть привлечён к судебной ответственности или арестован без признания Конституционным Судом его деятельности, не соответствующей Конституции РФРСН.

Статья 69. Депутат Всероссийского Съезда Народных Депутатов может быть отозван 2/3  избравших его собственников-совладельцев.

Статья 70. Всероссийский   Съезд   Народных Депутатов  избирает  Всероссийский Центральный  Комитет Координаторов  в  числе  не свыше 200   человек.



