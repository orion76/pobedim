Статья 74. Всероссийский Центральный Комитет Координаторов дает   общее   направление   деятельности всех органов самоуправляемого народа в  стране, объединяет и согласует работы по законодательству и координации  и наблюдает   за   проведением   в   жизнь Конституции  РФРСН, постановлений Всероссийского Съезда Народных Депутатов и центральных органов самоуправляемого народа.

Статья 75. Всероссийский Центральный Комитет Координаторов рассматривает и утверждает проекты постановлений и  иные  предложения, вносимые  Советом Народных Координаторов или отдельными ведомствами, а также издает собственные постановления и распоряжения.

Статья 76. Всероссийский Центральный Комитет Координаторов созывает Всероссийский Съезд Народных Депутатов, перед которым отчитывается о своей деятельности и делает доклады  по  общему  положению дел и  отдельным вопросам.

Статья 77. Всероссийский Центральный Комитет Координаторов образует Совет Народных Координаторов для общего  координирования  дел РФРСН и народные отделы  для   координирования работы отдельных народных самоуправляемых отраслей.

Статья 78. Всероссийский Центральный Комитет Координаторов организует референдумы и обсуждение проектов законов.

