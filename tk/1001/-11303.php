 9. Государственный план обязателен к применению для государственных предприятий и всех других специализированных комитетов. 
 8. Комитет финансов управляет государственными банками и обеспечивает стабильность финансовой системы и денежного обращения.
 9. Отраслевые комитеты ( пищевой промышленности, легкой промышленности, лесной промышленности, земледелия, зерновых и животноводческих совхозов,  путей сообщения,  связи,  водного транспорта, тяжелой промышленности, оборонной промышленности,  здравоохранения,  внутренней торговли,  внутренних дел, юстиции,  обороны, иностранных дел,  внешней торговли,  искусств,  высшей школы) разрабатывают специализированные стандарты и нормативы деятельности.
10. Работа в Президиуме и Комитетах подлежит оплате по единым тарифным ставкам (на основе образования, стажа работы по специальности, общего стажа работы) и не зависит от занимаемых должностей. 
    Необоснованный рост потребления госслужащими (или доходов связанных с ними лиц) является основанием для ревизии и судебного разбирательства. Поощрения возможны только через голосования.


###ГЛАВА 5   РАБОЧИЕ ГРУППЫ, КООРДИНАТОРЫ

1. Результат каждого голосования требует исполнения или надзор. Рабочая группа создаётся для реализации решения по итогам голосования.
2. Координатор - один из участников объединения (вече, комитета, рабочей группы), выбираемый соответствующим голосованием в объединении,  который отвечает за
  а) подтверждение личности участников,  
  б) закрепление ролей в объединении по итогам обсуждений,
  в) модерацию деятельности на предмет соответствия цели объединения,
  г) фиксирование результата достижения цели комитетом (рабочей группой). 
3.  В процессе работы  комитета решения принимаются открытым голосованием. Для реализации принимаемых решений могут создаваться соответствующие рабочие группы.
4.  В результате работы комитетов и рабочих групп должны быть конкретные результаты, свидетельствующие о завершении этапов работы комитета в достижении цели. Координатор фиксирует и объявляет результат.
5. Координатор может быть перевыбран в любой момент. Координатором может быть любой, кто проголосовал за себя и набрал большинство голосов в объединеннии.


###ГЛАВА 6 СУД И ПРОКУРАТУРА  

 1. Правосудие осуществляется Народными Судами.
 2. Рассмотрение дел во всех судах осуществляется с участием народных заседателей, кроме случаев, специально предусмотренных законом.
 3. Верховный Суд является высшим судебным органом. На Верховный Суд возлагается надзор за судебной деятельностью всех судебных органов.
 4. Верховный Суд и специальные суды создаются Народным Вече.
 5. Местные Суды - это народные суды создаваемые Местными Вече.
 6. Судопроизводство ведется на языке, утверждённом Территориальным Вече с обеспечением для лиц, не владеющих этим языком, полного ознакомлении с материалами дела через переводчика, а также права выступать на суде на родном языке.
 7. Разбирательство дел во всех судах открытое с обеспечением обвиняемому права на защиту. Суды ведутся справедливо: дела разбираются по существу, то есть, деяния классифицируются исходя из сути произошедшего вне зависимости от видимой юридической формы. 
 8. Судьи независимы и подчиняются только закону ( гл 3 ст 1).
 9. Высший надзор за точным исполнением законов всеми органами и подведомственными им учреждениями, равно как отдельными должностными лицами, а также гражданами возлагается на Народного Прокурора.
 10. Народный Прокурор назначается Народным Вече.
 11. Местные прокуроры назначаются Местными Вече.
 12. Органы прокуратуры осуществляют свои функции независимо от каких бы то ни было местных органов, подчиняясь только Народному Прокурору.
 13. Преступление судьи или прокурора - это преступление врага народа и карается предельно строго. 



