 3. Законы принимаются путём открытого бессрочного голосования с возможностью переголосовать в любой момент.
 4. Законы считаются принятыми при достижении 2/3 голосов от количества авторизованных участников голосования.
 5. Закон вступает в силу в момент соответствующего заявления сформированным  комитетом, отвечающего за его исполнение.
 6. Закон утрачивает свою силу при принятии альтернативного варианта в голосовании и вступлении в силу нового закона. При принятии нового закона в замен действующему, соответствующий новый комитет обязан урегулировать последствия изменения законодательной нормы. 
 7. При возникновении противоречий в законодательстве приоритет имеет норма с наибольшим количеством голосов. 
 8. При возникновении различий и противоречий между нормами Местных Вече и Народного Вече, нормы местного Вече применяются во взаимоотношениях между жителями соответствующей местности, а нормы Народного Вече между жителями различных территорий.
 9. Народное Вече формирует постоянно действующие органы государственного управления (Президиум Народного Вече, Верховный Суд, Прокуратура). 
 10. Местные Вече определяют структуру и формируют постоянно действующие органы самоуправления.
 11. Законы и постановления принимаются путём голосования граждан лично или через доверенных лиц  на официальной интернет-странице.
 12. Все законы и иные нормативные акты не должны противоречить действующей Конституции.
 13. Административное законодательство носит рекомендательный характер. Административные правила обязывают применять ответственность только при возникновении негативных последствий или очевидного риска таких последствий. То есть, если нарушение правила в конкретной ситуации не приводит к риску или последствию, которые должны предотвращаться   правилами, то привлечение к ответственности ради соблюдения юридической формы не допускается. Если административные правила не содержат подробного регламента, но в результате действия или бездействия, наступают последствия, то дела рассматриваются по существу относительно результата (наступивших последствий) в судебном порядке.
 14. Административные штрафы устанавливаются в процентах от уровня доходов, запрещено устанавливать штрафы в абсолютных значениях. Размеры штрафов определяются Народным Вече.
 15. Государственные стандарты в пищевой, химической и строительных отраслях обязательны к исполнению на уровне законов.
 16. Все юридические документы, в части расхождения по сути с нормами Народной Конституции, ничтожны.
 17. Каждый гражданин имеет право вносить в государственные органы и общественные организации предложения об улучшении их деятельности, критиковать недостатки в работе.  
 18. Должностные лица обязаны в установленные сроки рассматривать заявления граждан, давать на них ответы и принимать необходимые меры.
 19. Преследование за критику запрещается. Критика без предложений по улучшению не допускается.  Лица, преследующие за критику, привлекаются к ответственности.
 20. Граждане вправе объединения в общественные организации и партии. Дискриминация по признаку участия в той или иной организации (партии) запрещена.
 21. За участие в Народном Вече и Местном Вече запрещено платить и получать вознаграждение, а также принуждать к голосованию иными методами. Преследование за волеизъявление при голосовании является тяжким преступлением.


