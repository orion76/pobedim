#НАРОДНАЯ КОНСТИТУЦИЯ (ОБЩЕСТВЕННЫЙ ДОГОВОР)


###Преамбула 

 Народная конституция (общественный договор) составлена коллективно при участии всех желающих без исключений и с учётом итогов голосования на референдуме СССР 17 марта 1991г за сохранение СССР как обновлённой федерации. 

 Для выполнения общественно значимых функций народ на совете (вече) назначает на руководящие должности государственных служащих и наделяет их адекватными властными полномочиями в рамках народной конституции. 

Назначение на должности - это доверие народа, подкрепляемое уважением и стремлением помогать государственным служащим в выполнении их функций. 

Злоупотребление властью (народным доверием) государственными служащими является наитягчайшим преступлением, поскольку оно совершается против всего народа.


###Глава 1 ОБЩЕСТВЕННОЕ УСТРОЙСТВО

---  конституция = набор итогов  голосований с  2/3 голосов граждан , это нужно добавить

 1. СССР - Союз Советских Социалистических Республик - есть советское социалистическое федеративное государство трудящихся,  народное государство. 
 2. СССР состоит из РСФСР и социалистических республик заключивших федеративные договоры с РСФСР. 
 3. Конституция (общественный договор) - это основной закон и государственная идеология, регулирующие взаимоотношения в государстве. Государство является гарантом исполнения конституции.
 4. Идеологическая цель государства - способствовать счастливой жизни граждан, поддерживая семьи граждан и создавая условия для самореализации в обществе по принципу: от каждого по способности, каждому по потребности.
 5. Вся власть принадлежит народу. 
 6. Официальным государственным языком является русский язык.
 7. Земля и ее недра, воды, леса, наука, технологии - являются всенародным достоянием и находятся под государственной защитой. Граждане имеют право бесплатно пользоваться достоянием в установленном законом порядке.
 8. Экспорт, передача или продажа сырья для предпринимательской деятельности допустимы только государственным предприятиями в пределах утверждённого государственного плана. 
 9. Земля, занимаемая колхозами, закрепляется за ними в бесплатное и бессрочное пользование.
 10. Предоставление земельных и водных ресурсов государства в пользование иностранным лицам запрещено.
 11. Экономическая основа - социалистическая система хозяйства. Паразитизм считается неприемлемым. 
 12. Собственность подразделяется на общественную и личную. Личная собственность, используемая в предпринимательской деятельности, признаётся частной собственностью (активами). Управление общественной собственностью осуществляется государством.   
 13. Прибыль от собственности или предпринимательской деятельности всегда образуется за счёт накопленного общественного труда или от полученных от общества материалов и поэтому полностью принадлежит обществу. Ответственность за сохранность прибыли лежит на собственнике активов. 
 14. Законы разрабатываются на основе принципов жизни и критериев развития страны (глава 2 конституции).


###Глава 2  ПРИНЦИПЫ ЖИЗНИ И КРИТЕРИИ РАЗВИТИЯ СТРАНЫ
 1. Соборность: приоритет общих интересов над частными, решение проблем «миром». 
 2. Поликультурность: сохранение и уважение культур народностей на территориях коренных жителей.
 3. Централизация управления развития страны и её регионов.
 4. Самоуправление развития территорий, городов и сёл в рамках проекта развития страны. 
 5. Справедливость: законы должны быть справедливыми.
 6. Нестяжательство: достаток выше богатства; достаток растёт с богатством всего общества.
 7. Природосовместимость: сбережение среды обитания. Природа находится под защитой государства.
 8. Духовность: приоритет духовных ценностей над материальными.
 9. Ответственность: оценка народом принятых решений делегатами, контроль и оценка качества их исполнения.   
 10. Сохранение народа: а) демографический рост; б) воссоединение народа после разделения СССР.
    в) запрет вживлять чип в человека; вакцинация производится только государственными медицинскими учреждениями по утверждённым регламентам;
    г) эвтаназия запрещена; 
    д) запрет использовать органы человека без письменного согласия, полученного в установленном порядке.
 11. Социально-экономическая эффективность: первичность параметров социального развития перед экономическими показателями хозяйственной деятельности:
 а) расширение общедоступных благ: номенклатуры и объёма бесплатных благ для членов общества;
 б) улучшение качества жизни: рост продолжительности жизни, сокращение продолжительности рабочего дня, снижение пенсионного возраста, рост количества детей в семье, снижение числа разводов, снижение заболеваемости и других показателей.
 12. Суть результата (последствий) всегда имеет приоритет перед юридической формой.


###Глава 3  ОСНОВНЫЕ ПРАВА И ОБЯЗАННОСТИ, ГРАЖДАНСТВО 

-- изменения - обязанность участвовать в Вече 

 1. Осуществление прав и свобод неотделимо от исполнения обязанностей.  <br> Нарушение обязанностей влечёт ограничение в правах и свободах нарушителей. 
 2. Граждане обязаны участвовать в голосованиях Народного Вече и Местном Вече непосредственно самостоятельно или через доверенное лицо.
 3. Все обязаны соблюдать Конституцию и исполнять законы, честно относиться к общественному долгу, уважать правила социалистического общежития, быть непримиримым к антиобщественным поступкам, всемерно содействовать охране общественного порядка.
  Ответственность за нарушение Конституции предусмотрена в административном и уголовном кодексах.
 4. Все имеют право на самооборону: никто не может быть привлечён к ответственности за самооборону или защиту другого человека при нападении.
 5. Все равны перед законом во всех сферах жизни независимо от происхождения, социального и имущественного положения, пола, образования, языка, отношения к религии, рода и характера занятий, места жительства и других обстоятельств. 
 6. У всех есть право исповедовать любую религию или не исповедовать никакой, отправлять религиозные культы или вести атеистическую пропаганду. Возбуждение вражды и ненависти запрещается. Государство не поддерживает религии, политические партии и организации. Запрещено преподавать в школах программы религиозной тематики.
 7. Запрещаются идеологии и религии, если они: провозглашают превосходство одних групп людей над другими; 
  пропагандируют разрушение семьи через разделение прав женщин, мужчин, детей; пропагандируют секс, насилие, наркоманию, преступность, враждебность. 
 8. Всем обеспечивается неприкосновенность личности. Никто не может быть подвергнут аресту иначе как по постановлению суда или с санкции прокурора.
 9. Уважение личности, охрана прав и свобод - обязанность всех государственных органов, общественных организаций и должностных лиц.
 10. Все имеют право на судебную защиту от посягательств на честь и достоинство, жизнь и здоровье, на личную свободу и имущество.
 11. Все имеют право на помощь адвоката в суде для защиты невиновности или выявления обстоятельств для смягчения приговора. Адвокату запрещено скрывать факты вины обвиняемого. Сокрытие фактов адвокатом делает его соучастником преступления и влечёт за собой привлечение адвоката к уголовной ответственности.
 12. Все имеют право обжаловать действия должностных лиц, государственных и общественных органов. Жалобы должны быть рассмотрены в порядке и в сроки, установленные законом.
Действия должностных лиц, совершенные с нарушением закона, с превышением полномочий, ущемляющие права, могут быть обжалованы в суд.
 13. Все имеют право на возмещение ущерба, причиненного незаконными действиями государственных и общественных организаций, а также должностных лиц при исполнении ими служебных обязанностей. 
 14. Гражданами СССР признаются все, чьи родители - граждане СССР  и лица приобретшие гражданство в установленном законом порядке. 
 15. Утрата и лишение гражданства определяется законом. Лица, совершающие тяжкие преступления и преступления против общества, лишаются гражданства СССР в момент вступления в силу приговора суда. Возобновление гражданства возможно по решению Территориального Совета.
 16. Лица без гражданства СССР - это лица, имеющие иностранное гражданство или утратившие гражданство СССР; Лица без гражданства СССР не могут занимать руководящие и общественнозначимые должности в организациях.
 17. Граждане обязаны оберегать интересы государства, способствовать укреплению его могущества и авторитета.
 18. Граждане обязаны заботиться о сохранении исторических памятников и других культурных ценностей.
 19. Каждый гражданин обязан беречь и укреплять общественную, социалистическую собственность, как источник богатства и могущества родины, как источник достойной жизни всех трудящихся. Лица, покушающиеся на общественную, социалистическую собственность, являются врагами народа.
 20. Все обязаны бережно относиться к природе, охранять ее богатства.  
 21. Воинская служба в рядах Вооруженных Сил СССР представляет почетную обязанность граждан. Всеобщая воинская обязанность является законом. 
 22. Защита отечества - священный долг каждого гражданина. Измена родине: нарушение присяги, переход на сторону врага, нанесение ущерба военной мощи государства, шпионаж – караются по всей строгости закона, как самое тяжкое злодеяние.
 23. Интернациональный долг гражданина - содействовать развитию дружбы и сотрудничества с народами других стран, поддержанию и укреплению всеобщего мира.
 24. Все дееспособные граждане не моложе 18 лет кроме лиц с непогашенной судимостью обязаны голосовать в Народном и Территориальных Советах лично или через делегатов.
     Голоса граждан до 18 лет добавляются к голосам их родителей. Регистрация граждан для личного прямого участия в голосовании организуется также как и регистрация делегатов.  


###Глава 4 СЕМЬЯ, ЖИЛИЩЕ  

 1. Государство способствует укреплению семей граждан.
 2. Семья - это мужчина и женщина в добровольном браке, их дети и родители. Интересы всей семьи имеют приоритет перед личными правами каждого члена семьи в отдельности. 
 3. Основное предназначение семьи - рождение и воспитание детей (готовить их к общественно полезному труду, растить достойными членами социалистического общества). 
 4. Семьи граждан обеспечиваются детскими садами, женщинам при беременности предоставляются отпуска с сохранением содержания и широкая сеть родильных домов; производится выплата пособий на каждого ребенка в размере не ниже прожиточного минимума с момента регистрации беременности матери; питание детей в садах и школьных столовых бесплатно.
 5. Дети обязаны заботиться о родителях и оказывать им помощь.
 6. Супруги полностью равноправны. 
 7. Граждане имеют право на жилище. Это право обеспечивается развитием и охраной государственного и общественного жилищного фонда, содействием кооперативному и индивидуальному жилищному строительству. 
 8. Для индивидуального строительства по утвержденным нормативам государство предоставляет необходимые материалы. В случае продажи объекта индивидуального строительства, сделка рассматривается как предпринимательская с перечислением всей суммы прибыли в бюджет государства.
 9. Граждане должны бережно относиться к предоставленному им жилищу. Бесплатное жильё предоставляется в порядке очереди, которая зависит от возраста детей и общего социалистического трудового стажа. Правила распределения жилья утверждаются Территориальными Советами и исполняются соответствующими Территориальными Комитетами.
 10. Жилище, полученное бесплатно, не может быть предметом сделок, все операции с недвижимостью производятся через территориальные органы власти бесплатно.
 11. Неприкосновенность жилища граждан и тайна переписки охраняются законом.


###Глава 5 ТРУД И ПРЕДПРИНИМАТЕЛЬСКАЯ ДЕЯТЕЛЬНОСТЬ, ОТДЫХ, СОЦИАЛЬНОЕ ОБЕСПЕЧЕНИЕ   

 1. Труд в СССР является делом чести каждого способного к труду гражданина. 
 2. Государство обязано предоставить рабочие места всем гражданам на единых условиях государственных предприятий.
 3. Право на труд обеспечивается социалистической организацией народного хозяйства, неуклонным ростом производительных сил советского общества, устранением возможности хозяйственных кризисов и ликвидацией безработицы. 
 4. Труд оплачивается по принципу "от каждого по способности, каждому по труду", то есть, исходя из квалификации трудящегося и качества его труда. Оплата труда государством не зависит от занимаемой должности. Руководство государственного предприятия вправе предложить высокую должность трудящемуся, исходя из его квалификации, а трудящийся может отказаться от должности при условии несоответствия его квалификации.
 5. Минимальный уровень оплаты труда соответствует уровню цен прожиточного минимума и заработной плате трудящегося без квалификации, который определяется и обеспечивается через систему государственного планирования. 
 6. Труд в государственных организаций оплачивается сдельно по нормативам или по окладу в соответствии с единым квалификационным справочником профессий. Руководители получают ежемесячно оклад в размере минимального уровня оплаты труда и дополнительно, по итогу выполнения поставленных задач, сумму согласно квалификационному справочнику за всё время выполнения этих задач. При невыполнении поставленных задач, руководители не получают оплату за квалификацию.
 7. Предпринимательская деятельность рассматривается как свобода самореализации личности в труде. 
 8. Трудящиеся, занятые в государственных учреждениях, не могут участвовать в предпринимательской деятельности. 
 9. Предприниматели, привлекающие трудящихся, несут полную материальную ответственность по оплате труда трудящихся, охране здоровья, оплате налогов и социальных платежей в страховые компании.  Трудящиеся заключают с предпринимателями трудовые договоры с указанием размера вознаграждения, режима работы и отдыха. Права трудящихся защищаются законом.
 10. Сделки между предпринимателями регулируются договорами.
 11. Граждане, заключающие сделки с предпринимателями, защищены законом: при расторжении сделок кредиторы не могут требовать от граждан больше, чем возврат имущества в первоначальном виде; коммерческие риски несут предприниматели - брак подлежит замене; проценты, штрафы, пени, упущенная выгода и другие аналогичные платежи не взыскиваются.
 12. Трудящиеся имеют право на отдых. Право на отдых обеспечивается сокращением рабочего дня, установлением ежегодных отпусков рабочим и служащим с сохранением заработной платы, предоставлением для обслуживания трудящихся широкой сети санаториев, домов отдыха, клубов. 
 13. Пенсия - это зарплата за воспитание внуков и за сохранение преемственности поколений. Граждане имеют право на материальное обеспечение в старости, а также – в случае болезни и потери трудоспособности. 
 14. Пенсионный возраст устанавливается не выше 60 лет для мужчин и 55 лет для женщин. 
 15. Размер государственной пенсии зависит от трудового стажа в государственных организациях. Размер страховой пенсии зависит от суммы социальных платежей предпринимателей, перечисленных ими в пенсионные фонды.  
 16. Граждане имеют право на охрану здоровья. Это право обеспечивается бесплатной квалифицированной медицинской помощью, оказываемой государственными учреждениями здравоохранения; расширением сети учреждений для лечения и укрепления здоровья граждан; развитием и совершенствованием техники безопасности и производственной санитарии; проведением широких профилактических мероприятий; мерами по оздоровлению окружающей среды; особой заботой о здоровье подрастающего поколения, включая запрещение детского труда, не связанного с обучением и трудовым воспитанием; развертыванием научных исследований, направленных на предупреждение и снижение заболеваемости, на обеспечение долголетней активной жизни граждан.
 17. Граждане имеют право на образование и творчество. Это право обеспечивается бесплатностью всех видов образования, осуществлением всеобщего обязательного среднего образования молодежи, широким развитием профессионально-технического, среднего специального и высшего образования на основе связи обучения с жизнью, с производством; развитием заочного и вечернего образования; предоставлением государственных стипендий и льгот учащимся и студентам; бесплатной выдачей школьных учебников; возможностью обучения в школе на родном языке; созданием условий для самообразования.
 18. Гарантируется свобода научного, технического и художественного творчества. Она обеспечивается широким развертыванием научных исследований, изобретательской и рационализаторской деятельности, развитием литературы и искусства. Государство создает необходимые для этого материальные условия, оказывает поддержку добровольным обществам и творческим союзам, организует внедрение изобретений и рационализаторских предложений в народное хозяйство и другие сферы жизни.
 19. Права авторов, изобретателей и рационализаторов охраняются государством. Государство способствует бесплатному распространению научных знаний и передовых технологий внутри страны с учётом режима государственной безопасности.

 
###Глава 6 СВОБОДА СЛОВА ПРАВДЫ  

 1. Гражданам гарантируется свобода слова: право выражать правдивое мнение о происходящих процессах в обществе, основываясь на фактах, и высказывать предложения по улучшению этих процессов.
 2. Анонимность источников не признаётся, каждое слово при цитировании без ссылки на автора считается мнением говорящего.
 3. Запрещено искажать исторические факты и выдавать собственное мнение за факт вне зависимости от способа высказывания.  Использование выдержек из цитат с искажением сути высказывания признаётся ложью.
 4. При выявлении лжи автора все его произведения запрещаются, запрещается публичная деятельность, а архивные записи с участием автора в телепередачах и печатные издания помечаются ссылкой об уличении во лжи.
 5. Запрещается распространять ложь, цитируя высказывания лиц под запретом.
 6. Запрещена пропаганда террора, насилия, секса, извращений, ненормального поведения; новости в СМИ запрещено сопровождать соответствующими изображениями.
 7. Пропагандой является видеоматериал, художественные фильмы, новостные сюжеты и телевизионные передачи. Трансляция фильмов криминального характера должна быть ограничена.
 8. Государственные телеканалы должны показывать как надо жить, а не выискивать аморальные шокирующие примеры отдельных людей. Все транслируемые мультфильмы должны формировать образ правильного поведения в обществе.
 9. Материалы о проблемах  в обществе должны выявлять безответственность государственных служащих, чтобы уровень доверия граждан к делегатам был адекватным.
 10. Ответственность за цензуру лежит на руководителях СМИ.
 11. Запрет и снятие запрета на публикации авторов выносятся судом.


###Глава 7  УПРАВЛЕНИЕ ГОСУДАРСТВОМ, ПРИМЕНЕНИЕ ЗАКОНОВ  

 1. Государство управляется путём принятия законов на Народном Вече  и  Местных Вече, представляющих собой интернет сайт голосований "Держава" (pobedim.su).
