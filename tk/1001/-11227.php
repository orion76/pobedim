#Народная Конституция России                  

![]( https://pdsnpsr.ru/uploads/post/image/0/1/154/big_%D0%A1%D0%A3%D0%94.png)       

В проекте Народной Конституции России обоснуется, что только мобилизационная модель государственной власти, основанная на национализации стратегических отраслей, предприятий, земельных угодий, может привести к опережающему развитию страны.  
                   
В качестве одной из мер пресечения коррупции предлагается с одной стороны премирование чиновников в зависимости от показателей социально-экономического роста на подведомственной территории, а с другой – смертная казнь с конфискацией имущества.  
    
Для граждан в настоящей Конституции предусмотрено введение системы социальных паёв - именных счетов, на которые ежеквартально будет перечисляться часть бюджетных доходов, полученных от использования недр и государственной собственности. Приводится законопроект, утверждение которого возможно уже сегодня и пояснительная записка на данную тему.
    
Авторы Проекта народной Конституции:
    
- Андреев С.Ю., д.э.н., депутат Законодательного Собрания Санкт-Петербурга (1994 г. - 2007 г.), директор Центра регионального развития Северо-Западного филиала Академии народного хозяйства и государственной службы России, эксперт КС ООД «За национализацию и деприватизацию стратегических ресурсов страны»
    
- Филин В.И., к.с.н., член Бюро ЦС ООД «За национализацию и деприватизацию стратегических ресурсов страны», член Совета Общественной палаты при Президенте РФ (1994 г.), принимал участие в  разработке  Конституции РФ и являлся членом Конституционного Совещания.
    


