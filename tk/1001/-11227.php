Национальная идеология в России основывается на взаимной
ответственности государства и гражданина, семейных ценностях и любви к
Родине, желании человека приносить пользу другим и быть на этом пути
востребованным. Всё вместе, такие представления помножены на понимание
значимости России в мире, а значит – причастности гражданина нашей страны к
чему-то большему, чем его собственная судьба. Такая идеология означает
патриотизм в лучшем смысле этого слова: понятие любви к своей Родине не
принижает значимость других стран и государств.
Данная идеология составляет суть национальной идеи и поддерживается
государством в различных формах воспитания и пропаганды. 

