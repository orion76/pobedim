# ЧТО ДЕЛАТЬ Народу, если рабство и революция – одинаково гибельны для страны?  ____ 3 марта 2017   ____  [Geo-GEN](http://midgard-edem.org/?page_id=1330&user=3)   ____   
«Они вас сделали рабами – потому что вы позволили им быть господами…». Иса Вин-Ицис

![](http://images.myshared.ru/5/420463/slide_14.jpg)

Ситуация сегодня в странах бывшего Союза, да и по всему миру, парадоксальная.

С одной стороны людей душат всяческими налогами, растущими ценами, коммунальными платежами — притеснением по всем социальным правам. И при этом – усиливают карательные спец-структуры, защищающие зарвавшуюся власть и олигархию.

![](https://newsland.com/static/u/nlo/news_images/1020/big_1020311.jpg)

Что остается делать простым людям?

На первый взгляд, мы как один, должны выступить общим фронтом, подняв бунт-революцию и достойно наказать всех виновных в наших бедах.

Но с другой стороны, большинство СМИ и отдельные лидеры так явно, активно и открыто подталкивают народ к революции, что создается стойкое впечатление — это кому-то очень надо!

Но кому надо?

Да только тем, кто хочет использовать волнения и протесты — в качестве троянского коня для захвата ВСЕЙ власти. И при этом еще нагреть руки – разрушив до основания наши страны и перебив большинство народа! Кто же это? Это — социальные паразиты – от местного уровня и до мирового масштаба!

Что же нам делать? Крушить старый мир – и при этом самим оказаться в разрушенных хижинах?

![](http://xvesti.ru/images/stories/2015/blog/001/rev-1.jpg)

Или смириться с ситуацией, надеясь, что «наша хата с краю» — авось и останемся живы и целы?

Да, дилемма не простая: брутальная революция или полное порабощение.

Что выбрать?

Прежде чем окончательно решить, что делать, и как, – разберемся со всем этим ближе.

![](http://midgard-edem.org/wp-content/uploads/2016/12/1542.jpg)

Понятно, порабощения никто из нас не хочет. Справедливость, Свобода и Воля – сильнейшие мотивы жизни, данные нам свыше. Ими поступаться – значит не уважать себя, забыть свою связь с Богом.

Значит – Революция? Будем брать власть для народа под лозунгом «Мир хижинам – война дворцам»?

Увы, власть так просто никто не отдаст! Ведь, «власть – это всего всласть!». Кто ж от нее добровольно откажется? Никто! Значит будет отчаянное сопротивление по всем фронтам – с использованием всех критических национальных, религиозных и т.д. факторов . Что априори приведет к разрушению социальной и экономической инфраструктуры государства. Что СОВЕРШЕННО НЕ НУЖНО самому Народу!

Где же выход? Как избавиться от усиливающегося с каждым днем рабства, и при этом не скатиться к кровавой гражданской войне всех со всеми?

![](http://midgard-edem.org/wp-content/uploads/2016/10/Матрица-300x225.jpg)

Логически, выход один – оставаясь в рамках существующих законов, использовать все легальные возможности для мирного перехвата власти. В первую очередь путем растворения их кажущейся силы властьимущих методом игнорирования их новых, явно антинародных, указов/приказов. Плюс перехват опорных точек законодательной власти – начиная с местных, городских, районных и т.д., советов – и переход их в руки самого Народа. Для этого, используя  [механизмы Белого КОНа](http://midgard-edem.org/?p=1481) по формированию системы реального народовластия, начать массово, ПАРАЛЛЕЛЬНО старой системе, выстраивать основы КОНа снизу – по новым,  [природо-социальным принципам](http://midgard-edem.org/?p=943) ( [http://midgard-edem.org/?p=943](http://midgard-edem.org/?p=943))! Т.е. активно выстраивать горизонтальные и вертикальные структуры само-организации и само-управления – в виде советов всех сфер и отраслей жизнедеятельности, начиная от Первичных Коллективов – и заканчивая Центральным Советом Капитанов/Комиссаров —  [http://midgard-edem.org/?cat=53](http://midgard-edem.org/?cat=53)  —  [СХЕМЫ №№ 6 и 7](http://midgard-edem.org/?cat=53).

![](http://midgard-edem.org/wp-content/uploads/2017/01/bb015-300x179.jpg)

