Так, в комплексе используя все легальные механизмы перехвата власти снизу и мирные методы воздействия на ее верхние эшелоны — мы перехватим рычаги управления у тех, кто нами сегодня правит. Ведь, нижние органы само-управления, в своей совокупности, и есть та самая власть, что и формируют власть центральную.

